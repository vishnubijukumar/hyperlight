use core::mem::size_of;

/// Same shape as amd64 `ExceptionInfo` for API compatibility; not populated by hardware on s390x.
#[repr(C)]
pub struct ExceptionInfo {
    pub error_code: u64,
    pub rip: u64,
    pub cs: u64,
    pub rflags: u64,
    pub rsp: u64,
    pub ss: u64,
}

const _: () = assert!(size_of::<ExceptionInfo>() == 8 * 6);
